/*valueOf() ->
- The valueOf() method returns the array itself.
- The valueOf() method does not change the original Array.
- Normally, you will not use it in your code.
*/

const fruits = ["Apple","Mango","Banana","Orange"];
console.log(fruits);                                 //[ 'Apple', 'Mango', 'Banana', 'Orange' ]
console.log(fruits.valueOf());                       //[ 'Apple', 'Mango', 'Banana', 'Orange' ]